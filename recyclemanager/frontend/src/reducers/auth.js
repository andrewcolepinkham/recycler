import {USER_LOADED, USER_LOADING, AUTH_ERROR} from "../actionst/types"; 

const initialState = {
    token: localStorage.getItem("token"), 
    isAuthenticated: null, 
    isLoading: false, 
    user: null
}

export default function(state=initialState, action){
    switch(action.type){
        case USER_LOADING: 
            return {
                ...state, 
                isLoading: true
            }
        case USER_LOADED: 
            return {
                ...state, 
                isAuthenticated: true,  
                isLoading: false, 
                user: action.payload
            }
        case AUTH_ERROR: 
            localStorage.removeItem('token'); 
            return {
                ...state,
                token: null, 
                user:null,
                isAuthenticated:null, 
                isLoading:null
            }
        default: 
            return state;

    }

}


